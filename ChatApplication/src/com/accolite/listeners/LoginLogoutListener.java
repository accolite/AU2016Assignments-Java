	/**
     * @see HttpSessionListener#sessionDestroyed(HttpSessionEvent)
     */
    public void sessionDestroyed(HttpSessionEvent arg0)  { 
         
    	ServletContext sc=arg0.getSession().getServletContext();
    	Message msg=new Message();
		msg.setMessage("Logged out");
		msg.setUsername(arg0.getSession().getAttribute("User").toString());
		String name=arg0.getSession().getAttribute("User").toString();
		ActiveUsers users=(ActiveUsers) sc.getAttribute("ACTIVE_USERS");
		for(int i=0;i<users.getActiveUsers().size();i++)
		{
			User au=users.getActiveUsers().get(i);
			if(au.getUsername().equals(name))
				users.getActiveUsers().remove(i);
		}
		arg0.getSession().setAttribute("User",null);
		MessageList lst=(MessageList) sc.getAttribute("MESSAGES");
		lst.getMessageLst().add(msg);
		sc.setAttribute("MESSAGES",lst);
		
    }

